    private void load_users(String string) {
        DefaultTableModel model = (DefaultTableModel)table1.getModel();
        model.setRowCount(0);
        Object[] row = new Object[3];
        for(int i = 0; i < ul.size(); i++)
        {
            row[0] = ul.get(i).username;
            row[1] = ul.get(i).pwd;
            if(ul.get(i).type==1) row[2] = "Admin";
            else row[2] = "Secretaire";
            model.addRow(row);
        }
    }
