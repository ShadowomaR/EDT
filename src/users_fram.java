    private void jMenuItem1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuItem1MousePressed
        int id;
        try {
            id=ul.get(table1.getSelectedRow()).id;
            int f=JOptionPane.showConfirmDialog(null,"Vouler vous suprimer cette utilisateur","Suprimer", JOptionPane.YES_NO_OPTION);
            if (f==0) {
                JOptionPane.showMessageDialog(null,u.suprimer(id));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Selectioner un utilisateur");
        }
        ul=u.getuserslist("", "");
        load_users("");
    }//GEN-LAST:event_jMenuItem1MousePressed

